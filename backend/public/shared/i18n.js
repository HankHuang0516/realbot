// Portal version for sync tracking
const PORTAL_VERSION = '1.2.0';

class I18n {
    constructor() {
        const saved = localStorage.getItem('eclaw-language');
        if (saved && TRANSLATIONS[saved]) {
            this.lang = saved;
        } else {
            // Auto-detect from browser language
            const browserLang = (navigator.language || navigator.userLanguage || 'en').toLowerCase();
            if (browserLang.startsWith('zh-cn') || browserLang.startsWith('zh-hans')) {
                this.lang = 'zh-CN';
            } else if (browserLang.startsWith('zh')) {
                this.lang = 'zh';
            } else if (browserLang.startsWith('ja')) {
                this.lang = 'ja';
            } else if (browserLang.startsWith('ko')) {
                this.lang = 'ko';
            } else if (browserLang.startsWith('th')) {
                this.lang = 'th';
            } else if (browserLang.startsWith('vi')) {
                this.lang = 'vi';
            } else if (browserLang.startsWith('id') || browserLang.startsWith('in')) {
                this.lang = 'id';
            } else {
                this.lang = 'en';
            }
        }
        this.observers = [];
    }

    setLanguage(lang) {
        if (!TRANSLATIONS[lang]) return;
        this.lang = lang;
        localStorage.setItem('eclaw-language', lang);
        this.apply();
        this.observers.forEach(cb => cb(lang));
    }

    t(key, params = {}) {
        const dict = TRANSLATIONS[this.lang] || TRANSLATIONS['en'];
        let str = dict[key] || TRANSLATIONS['en'][key] || key;

        // Simple parameter replacement {name}
        Object.keys(params).forEach(k => {
            str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), params[k]);
        });
        return str;
    }

    apply() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (key) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    if (el.hasAttribute('placeholder')) {
                        el.placeholder = this.t(key);
                    }
                } else {
                    el.innerHTML = this.t(key);
                }
            }
        });

        // Update html lang attribute
        const langMap = { 'zh': 'zh-TW', 'zh-CN': 'zh-CN', 'ja': 'ja', 'ko': 'ko', 'th': 'th', 'vi': 'vi', 'id': 'id' };
        document.documentElement.lang = langMap[this.lang] || 'en';
    }

    onLanguageChange(cb) {
        this.observers.push(cb);
    }
}

// Global instance
const i18n = new I18n();

// Auto-apply on load
document.addEventListener('DOMContentLoaded', () => {
    i18n.apply();
});
